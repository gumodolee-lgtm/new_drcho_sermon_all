// ── 유틸리티 함수 ──

function h1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 480, after: 200 },
    children: [new TextRun({ text, bold: true, size: 36, font: FONT, color: "1F4E79" })],
  });
}

function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 360, after: 160 },
    children: [new TextRun({ text, bold: true, size: 28, font: FONT, color: "2E75B6" })],
  });
}

function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 280, after: 120 },
    children: [new TextRun({ text, bold: true, size: 24, font: FONT, color: "2F5496" })],
  });
}

function body(text) {
  return new Paragraph({
    spacing: { after: 140, line: 340 },
    children: [new TextRun({ text, size: 22, font: FONT })],
  });
}

function bold(text) {
  return new Paragraph({
    spacing: { after: 120, line: 320 },
    children: [new TextRun({ text, bold: true, size: 22, font: FONT })],
  });
}

function quote(text) {
  return new Paragraph({
    spacing: { after: 120, line: 300 },
    indent: { left: 480, right: 480 },
    children: [new TextRun({ text: `"${text}"`, size: 21, font: FONT, italics: true, color: "444444" })],
  });
}

function src(file, date) {
  return new Paragraph({
    spacing: { after: 80 },
    indent: { left: 480 },
    children: [new TextRun({ text: `— 설교 원문: ${file} (${date})`, size: 19, font: FONT, color: "888888" })],
  });
}

function bible(verse, content) {
  return new Paragraph({
    spacing: { after: 120, line: 300 },
    indent: { left: 240 },
    children: [
      new TextRun({ text: `${verse}  `, bold: true, size: 22, font: FONT, color: "1F4E79" }),
      new TextRun({ text: content, size: 21, font: FONT, italics: true }),
    ],
  });
}

function bibleExplain(text) {
  return new Paragraph({
    spacing: { after: 100, line: 300 },
    indent: { left: 480 },
    children: [new TextRun({ text, size: 20, font: FONT, color: "555555" })],
  });
}

function divider() {
  return new Paragraph({
    spacing: { before: 300, after: 300 },
    children: [new TextRun({ text: "\u2500".repeat(50), size: 20, font: FONT, color: "AAAAAA" })],
  });
}

function empty() {
  return new Paragraph({ spacing: { after: 80 }, children: [new TextRun({ text: "", size: 10 })] });
}

function keyword(text) {
  return new Paragraph({
    spacing: { before: 100, after: 100 },
    indent: { left: 240 },
    children: [new TextRun({ text: `[반복 강조 핵심 표현] ${text}`, bold: true, size: 21, font: FONT, color: "C00000" })],
  });
}

function center(text, size, color) {
  return new Paragraph({
    alignment: AlignmentType.CENTER,
    spacing: { after: 160 },
    children: [new TextRun({ text, bold: true, size: size || 28, font: FONT, color: color || "1F4E79" })],
  });
}

