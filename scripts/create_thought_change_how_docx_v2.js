function textRun(text, options = {}) {
  return new TextRun({
    text,
    font: FONT,
    size: options.size || 22,
    bold: options.bold || false,
    color: options.color,
    italics: options.italics || false,
  });
}

function body(text, options = {}) {
  return new Paragraph({
    alignment: options.alignment,
    spacing: { after: 140, line: 340, before: options.before || 0 },
    children: [
      textRun(text, {
        size: options.size || 22,
        bold: options.bold || false,
        color: options.color,
        italics: options.italics || false,
      }),
    ],
  });
}

function centered(text, size = 28, color = "1F4E79", bold = false) {
  return body(text, {
    alignment: AlignmentType.CENTER,
    size,
    color,
    bold,
  });
}

function heading(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 260, after: 140 },
    children: [textRun(text, { bold: true, size: 30, color: "1F4E79" })],
  });
}

function label(text) {
  return new Paragraph({
    spacing: { before: 100, after: 80 },
    children: [textRun(text, { bold: true, size: 24, color: "2F5597" })],
  });
}

function bullet(text) {
  return new Paragraph({
    bullet: { level: 0 },
    spacing: { after: 80, line: 320 },
    children: [textRun(text)],
  });
}

function verseReference(ref) {
  return new Paragraph({
    spacing: { before: 90, after: 60 },
    children: [textRun(ref, { bold: true, size: 23, color: "1F4E79" })],
  });
}

function verseText(text) {
  return new Paragraph({
    spacing: { after: 70, line: 320 },
    indent: { left: 180 },
    children: [textRun(text, { italics: true, color: "333333" })],
  });
}

function verseReason(text) {
  return new Paragraph({
    spacing: { after: 110, line: 320 },
    indent: { left: 300 },
    children: [
      textRun("이 구절이 관련되는 이유: ", { bold: true, size: 20, color: "666666" }),
      textRun(text, { size: 20, color: "666666" }),
    ],
  });
}

function divider() {
  return new Paragraph({
    spacing: { before: 120, after: 120 },
    children: [textRun("─".repeat(40), { color: "B7B7B7", size: 18 })],
  });
}

